
print("********* range(10) or range(0,10,1) ************")
for i in range(10):
    print(i)
    
print("********* While i < 10************")
i = 0
while i < 10:
    print(i)
    i = i + 1
    
    
print("********* range(0,10,2) increment by 2 ************")
for i in range(0,10,2):
    print(i)
    
print("********* While i < 10 increment by 2 ************")
i = 0
while i < 10:
    print(i)
    i = i + 2

